namespace Videra.SurfaceCharts.Core;

/// <summary>
/// Represents the projection mode used by the chart camera.
/// </summary>
public enum SurfaceProjectionMode
{
    /// <summary>
    /// Uses a perspective camera projection.
    /// </summary>
    Perspective = 0,

    /// <summary>
    /// Uses an orthographic camera projection.
    /// </summary>
    Orthographic = 1
}
