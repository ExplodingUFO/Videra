namespace Videra.SurfaceCharts.Avalonia.Controls;

/// <summary>
/// Identifies the background behavior for a Plot snapshot export.
/// </summary>
public enum PlotSnapshotBackground
{
    /// <summary>
    /// The snapshot background is fully transparent.
    /// </summary>
    Transparent,

    /// <summary>
    /// The snapshot background is filled with an opaque color.
    /// </summary>
    Opaque,
}
